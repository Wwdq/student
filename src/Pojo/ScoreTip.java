package Pojo;

public class ScoreTip {
    private int eno;
    private int top;
    private int lastone;
    private double avgs;
    private int total;
    private int number;
    private double bai;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public double getAvgs() {
        return avgs;
    }

    public void setAvgs(double avgs) {
        this.avgs = avgs;
    }

    public int getLastone() {
        return lastone;
    }

    public void setLastone(int lastone) {
        this.lastone = lastone;
    }

    public int getTop() {
        return top;
    }

    public void setTop(int top) {
        this.top = top;
    }

    public int getEno() {
        return eno;
    }

    public void setEno(int eno) {
        this.eno = eno;
    }

    public double getBai() {
        return bai;
    }

    public void setBai(double bai) {
        this.bai = bai;
    }
}
